package com.example.letngo;

public class ReadWriteUserDeatils {


    public String UserName, Email, Mobile, Password, FirstName, LastName, Birthday, Age, Gender, Address, Description;

    public ReadWriteUserDeatils(String un, String em, String mb, String pass, String fn, String ln, String bd, String ag, String gd, String ad, String dc) {

        super();


        this.UserName = un;
        this.Email = em;
        this.Mobile = mb;
        this.Password = pass;

        this.FirstName = fn;
        this.LastName = ln;
        this.Birthday = bd;
        this.Age = ag;

        this.Gender = gd;
        this.Address = ad;
        this.Description = dc;

    }

}
